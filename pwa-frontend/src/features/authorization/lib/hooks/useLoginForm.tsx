import { useUnit } from "effector-react";
import {
  $loginForm,
  loginFormSubmitted,
  nameChanged,
  $loginFormErrors,
  emailChanged,
  passwordChanged,
  $isLoginFormValid,
  logoutEvent,
  $jwt_token,
  appStarted,
  $authError,
  $isUserLoggedIn,
  openModalEvent,
  $modalOpen,
  $userInfo,
  $modeModal,
  changeModeEvent,
  authSuccess,
} from "../../model/stores";

export const useLoginForm = () => {
  const {
    form,
    submitEvent,
    nameChangeEvent,
    openModal,
    logout,
    isUserLoggedIn,
    isModalOpen,
    modeModal,
    authError,
    appStartedEvent,
    changeMode,
    isFormValid,
    errors,
    userInfo,
    token,
    emailChangeEvent,
    passwordChangeEvent,
  } = useUnit({
    form: $loginForm,
    logout: logoutEvent,
    token: $jwt_token,
    appStartedEvent: appStarted,
    submitEvent: loginFormSubmitted,
    authError: $authError,
    modeModal: $modeModal,
    isUserLoggedIn: $isUserLoggedIn,
    openModal: openModalEvent,
    nameChangeEvent: nameChanged,
    isModalOpen: $modalOpen,
    authSuccess: authSuccess,
    userInfo: $userInfo,
    changeMode: changeModeEvent,
    emailChangeEvent: emailChanged,
    isFormValid: $isLoginFormValid,
    passwordChangeEvent: passwordChanged,
    errors: $loginFormErrors,
  });

  return {
    form,
    submit: () => submitEvent(form),
    nameChangeEvent,
    emailChangeEvent,
    isUserLoggedIn,
    authSuccess,
    isModalOpen,
    openModal,
    changeMode,
    logout,
    modeModal,
    userInfo,
    authError,
    appStartedEvent,
    token,
    isFormValid,
    passwordChangeEvent,
    errors,
  };
};
